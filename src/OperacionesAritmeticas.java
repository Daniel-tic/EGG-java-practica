
public class OperacionesAritmeticas {

    public static void main(String[] args) {
        //definimos variables
        int a = 10;
        int b = 5;
        //suma
        int c = a + b;
        System.out.println("a + b = " + c);
        //resta
        int d = a - b;
        System.out.println("a - b = "+ d);
        //multiplicacion
        int e = a * b;
        System.out.println("a * b = " + e);
        //division
        int f = a / b;
        System.out.println("a / b = " + f);
        //modulo
        int g = a % b;
        System.out.println("a % b = " + g);
    }
}
